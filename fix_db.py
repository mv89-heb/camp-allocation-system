import pandas as pd

df = pd.read_csv("data/inventory.csv")

# ======================
# מזהה חדר (מספר)
# ======================
df["room_id"] = df["apartment"].astype(str).str.extract(r'(\d+)')

# ======================
# מיפוי שמות דירות
# ======================
city_map = {
    "101": "באר שבע", "102": "באר שבע",
    "103": "אשקלון", "104": "אשקלון",
    "105": "אשדוד", "106": "אשדוד",
    "107": "אילת", "108": "אילת",

    "201": "ירושלים", "202": "ירושלים",
    "203": "חברון", "204": "חברון",
    "205": "רחובות", "206": "רחובות",
    "207": "יפו", "208": "יפו",
    "209": "יפו", "210": "יפו",
    "211": "יבנה", "212": "יבנה",

    "301": "זכרון", "302": "זכרון",
    "303": "זכרון", "304": "זכרון",
    "305": "עכו", "306": "עכו",
    "307": "עכו", "308": "עכו",
    "309": "עכו", "310": "עכו",
    "311": "מטולה", "312": "מטולה",
}

# ======================
# שם תצוגה
# ======================
def get_display(row):
    apt = str(row["apartment"])

    if "קרוון" in apt:
        return apt  # קרוונים נשארים כמו שהם

    room = row["room_id"]
    city = city_map.get(room, "")

    if city:
        return f"{city} {room}"

    return apt

df["display_name"] = df.apply(get_display, axis=1)

# ======================
# תקן
# ======================
def get_standard(name):
    if "קרוון קטן" in name:
        return 2
    elif "קרוון" in name:
        return 8
    elif "מדריכים" in name or "אב בית" in name or "שירות" in name:
        return None
    return 6

df["beds"] = df["display_name"].apply(get_standard)
df["mattresses"] = df["beds"]

# ======================
# נתונים בפועל (לפי מספר!)
# ======================
actual_rooms = {
    "101": (4,4), "102": (5,5), "103": (4,3), "104": (4,4),
    "105": (4,4), "106": (4,4), "107": (4,4), "108": (4,4),

    "201": (4,5), "202": (4,4), "203": (5,5), "204": (4,3),
    "205": (5,5), "206": (4,5), "207": (4,6), "208": (5,5),
    "209": (5,5), "210": (4,5), "211": (5,5), "212": (4,4),

    "301": (4,4), "302": (5,4), "303": (4,4), "304": (5,5),
    "305": (4,4), "306": (4,3), "307": (4,4), "308": (4,5),
    "309": (5,5), "310": (4,4), "311": (4,4), "312": (4,4),
}

caravan_data = {
    "קרוון 1": (2,3),
    "קרוון 2": (6,5),
    "קרוון 3": (5,5),
    "קרוון 4": (5,5),
    "קרוון 6": (2,0),
    "קרוון 7": (2,0),
    "קרוון 8": (2,2),
    "קרוון 9": (5,5),
}

def get_actual(row):
    name = row["display_name"]

    if "קרוון" in name:
        return caravan_data.get(name, (0,0))

    room = row["room_id"]
    return actual_rooms.get(room, (0,0))

df["beds_actual"] = df.apply(lambda r: get_actual(r)[0], axis=1)
df["mattresses_actual"] = df.apply(lambda r: get_actual(r)[1], axis=1)

# ======================
# להציג בדאשבורד
# ======================
df["apartment"] = df["display_name"]

# ======================
# ניקוי
# ======================
df = df.drop(columns=["display_name", "room_id"])

# ======================
# שמירה
# ======================
df.to_csv("data/inventory.csv", index=False)

print("✅ עודכן מושלם: תקן + בפועל + שמות דירות יציבים")